// 防止头文件被重复包含（C++ 标准语法）
// 作用：避免同一个头文件被多次 #include 导致重复定义错误
#pragma once

// 包含DX12工具函数：创建缓冲区、编译着色器、检查错误
#include "../../Common/d3dUtil.h"
// 包含数学工具：矩阵、向量、单位矩阵初始化
#include "../../Common/MathHelper.h"
// 包含上传缓冲区模板类：专门用于CPU→GPU上传数据
#include "../../Common/UploadBuffer.h"

//=====================================================================================
// 结构体：ObjectConstants（物体常量）
// C++ 语法：struct = 数据结构体，用来打包一组相关数据
// DX12 作用：传给 GPU 的【每个物体专属数据】，主要是世界矩阵
// 每个物体都有自己的世界矩阵（位置/旋转/缩放），所以每个物体需要一份此结构
//=====================================================================================
struct ObjectConstants
{
    // 世界矩阵：把物体从局部空间变换到世界空间
    // MathHelper::Identity4x4() = 初始化为单位矩阵（C++ 成员默认初始化）
    DirectX::XMFLOAT4X4 World = MathHelper::Identity4x4();
};

//=====================================================================================
// 结构体：PassConstants（全局渲染常量）
// DX12 作用：传给 GPU 的【全局数据】，一帧只需要一份
// 包含相机矩阵、屏幕尺寸、时间等所有物体共用的数据
//=====================================================================================
struct PassConstants
{
    // 观察矩阵：相机视角矩阵
    DirectX::XMFLOAT4X4 View = MathHelper::Identity4x4();
    // 观察矩阵的逆矩阵
    DirectX::XMFLOAT4X4 InvView = MathHelper::Identity4x4();
    // 投影矩阵：3D → 2D 透视投影
    DirectX::XMFLOAT4X4 Proj = MathHelper::Identity4x4();
    // 投影矩阵的逆矩阵
    DirectX::XMFLOAT4X4 InvProj = MathHelper::Identity4x4();
    // 观察*投影 矩阵
    DirectX::XMFLOAT4X4 ViewProj = MathHelper::Identity4x4();
    // 观察*投影 的逆矩阵
    DirectX::XMFLOAT4X4 InvViewProj = MathHelper::Identity4x4();

    // 相机在世界空间中的位置
    DirectX::XMFLOAT3 EyePosW = {0.0f, 0.0f, 0.0f};
    // C++ / DX12 重要知识点：填充字节（对齐用）
    // 因为HLSL常量缓冲区要求 4 个 float 为一组，这里用一个 float 补齐
    float cbPerObjectPad1 = 0.0f;

    // 渲染目标大小（窗口宽高）
    DirectX::XMFLOAT2 RenderTargetSize = {0.0f, 0.0f};
    // 渲染目标大小的倒数（1/width, 1/height），着色器里常用
    DirectX::XMFLOAT2 InvRenderTargetSize = {0.0f, 0.0f};

    // 近裁剪面距离
    float NearZ = 0.0f;
    // 远裁剪面距离
    float FarZ = 0.0f;
    // 程序启动到现在的总时间
    float TotalTime = 0.0f;
    // 帧间隔时间（上一帧到这一帧的时间）
    float DeltaTime = 0.0f;
};

//=====================================================================================
// 结构体：Vertex（顶点数据）
// DX12 作用：定义一个顶点包含哪些数据
// 每个顶点 = 位置 + 颜色
//=====================================================================================
struct Vertex
{
    // 顶点 3D 坐标（x,y,z）
    DirectX::XMFLOAT3 Pos;
    // 顶点颜色（r,g,b,a）
    DirectX::XMFLOAT4 Color;
};

//=====================================================================================
// 结构体：FrameResource（帧资源）
// C++ 语法：这是一个完整的类式结构体，有构造函数、析构函数、成员变量
// DX12 核心作用：
//    为每一帧渲染提供独立的 GPU 资源，避免 CPU 和 GPU 同时访问同一块内存
//    通常会创建 3 个 FrameResource，实现 CPU/GPU 并行（帧重叠）
//=====================================================================================
struct FrameResource
{
public:
    //-----------------------------------------------------------------------------
    // C++ 语法：构造函数
    // 作用：创建帧资源时，自动创建命令分配器 + 2个上传缓冲区
    // 参数：DX12设备、全局常量数量、物体常量数量
    //-----------------------------------------------------------------------------
    FrameResource(ID3D12Device *device, UINT passCount, UINT objectCount);

    //-----------------------------------------------------------------------------
    // C++ 语法：= delete
    // 作用：【禁止拷贝构造】
    // 因为帧资源包含 GPU 资源，不能复制，否则会导致资源重复释放/崩溃
    //-----------------------------------------------------------------------------
    FrameResource(const FrameResource &rhs) = delete;

    //-----------------------------------------------------------------------------
    // C++ 语法：= delete
    // 作用：【禁止赋值操作】
    // 理由同上，GPU 资源不允许赋值拷贝
    //-----------------------------------------------------------------------------
    FrameResource &operator=(const FrameResource &rhs) = delete;

    //-----------------------------------------------------------------------------
    // C++ 语法：析构函数 ~函数名()
    // 作用：对象销毁时自动调用，释放 GPU 资源
    //-----------------------------------------------------------------------------
    ~FrameResource();

    //-----------------------------------------------------------------------------
    // DX12 命令分配器
    // 作用：存储命令列表的内存
    // 为什么每一帧要独立一个？
    // 因为 GPU 还在执行上一帧的命令时，CPU 不能重置它
    // 所以必须每一帧用自己的分配器
    //-----------------------------------------------------------------------------
    Microsoft::WRL::ComPtr<ID3D12CommandAllocator> CmdListAlloc;

    //-----------------------------------------------------------------------------
    // 上传缓冲区：存储全局常量（PassConstants）
    // std::unique_ptr：C++ 智能指针，自动释放内存，安全不泄漏
    //-----------------------------------------------------------------------------
    std::unique_ptr<UploadBuffer<PassConstants>> PassCB = nullptr;

    //-----------------------------------------------------------------------------
    // 上传缓冲区：存储所有物体的常量（ObjectConstants）
    //-----------------------------------------------------------------------------
    std::unique_ptr<UploadBuffer<ObjectConstants>> ObjectCB = nullptr;

    //-----------------------------------------------------------------------------
    // 围栏值（Fence Value）
    // DX12 同步机制：
    // 用来标记这一帧的命令被 GPU 执行到哪个位置
    // CPU 可以通过这个值判断：GPU 是否已经用完这一帧的资源
    //-----------------------------------------------------------------------------
    UINT64 Fence = 0;
};