// C++ 语法：析构函数 ~类名()
// 作用：对象销毁时自动调用
// 这里是空的，因为成员变量都是智能指针(ComPtr / unique_ptr)
// 智能指针会【自动释放】GPU 资源，不需要手动写代码
//=====================================================================================
FrameResource::~FrameResource()
{
    // 空函数体 → 智能指针自动释放资源
}