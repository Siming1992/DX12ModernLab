// 包含头文件，这样才能使用 FrameResource 结构体的定义
#include "FrameResource.h"

//=====================================================================================
// C++ 语法：构造函数的类外实现
// 作用：创建 帧资源(FrameResource) 对象时，自动初始化 GPU 资源
// 参数：
//   device        - DX12 设备（用来创建所有GPU资源）
//   passCount     - 全局常量缓冲区需要多少个元素（一般=1）
//   objectCount   - 物体常量缓冲区需要多少个元素（场景中有多少个物体就填多少）
//=====================================================================================
FrameResource::FrameResource(ID3D12Device *device, UINT passCount, UINT objectCount)
{
    //---------------------------------------------------------------------------------
    // DX12 API：创建 命令分配器(CommandAllocator)
    // C++ 语法：ThrowIfFailed 是工具函数，创建失败会自动抛异常，方便调试
    //
    // 作用：命令分配器是 GPU 用来存储命令列表的内存
    // 为什么每帧都要创建一个？
    // 因为 GPU 正在执行上一帧命令时，CPU 不能修改那块内存
    //---------------------------------------------------------------------------------
    ThrowIfFailed(device->CreateCommandAllocator(
        D3D12_COMMAND_LIST_TYPE_DIRECT,              // 命令类型：直接命令列表（渲染用）
        IID_PPV_ARGS(CmdListAlloc.GetAddressOf()))); // 输出创建好的分配器到 ComPtr 智能指针

    //---------------------------------------------------------------------------------
    // C++ 语法：std::make_unique<类型>(构造参数)
    // 作用：创建一个 unique_ptr 智能指针管理的对象，自动释放内存，不会泄漏
    //
    // DX12 作用：创建 上传缓冲区(UploadBuffer)
    // 用途：存储全局常量（相机、矩阵、时间等），传给着色器
    // 参数：设备、元素数量、是否是常量缓冲区（true=是）
    //---------------------------------------------------------------------------------
    PassCB = std::make_unique<UploadBuffer<PassConstants>>(device, passCount, true);

    //---------------------------------------------------------------------------------
    // 同上，创建存储【每个物体】世界矩阵的上传缓冲区
    //---------------------------------------------------------------------------------
    ObjectCB = std::make_unique<UploadBuffer<ObjectConstants>>(device, objectCount, true);
}

